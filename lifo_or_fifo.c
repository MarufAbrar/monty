#include "monty.h"

/**
  * lifo - changes the addition to the stack to LIFO
  * @stack: NULL
  * @line_number: 0
  * Return: nothing, void
  */
void lifo(stack_t **stack, unsigned int line_number)
{
	(void) stack;
	(void) line_number;

	element->IFO = 0;
}

/**
  * fifo - changes the addition to the stack to FIFO
  * @stack: NULL
  * @line_number: 0
  * Return: nothing, void
  */
void fifo(stack_t **stack, unsigned int line_number)
{
	(void) stack;
	(void) line_number;

	element->IFO = 1;
}
